package com.alex.cursojava.aula10;

public class For {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            System.out.println("For com incremento: " + i);
        }

        for (int i = 5; i > 0; i--) {
            System.out.println("For com decremento: " + i);
        }
    }
}
