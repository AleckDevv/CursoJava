package com.alex.cursojava.aula6;

public class OperadoresRelacionais {
    public static void main(String[] args) {
        /*
         * Operadores relacionais
         * == igual a
         * != diferente de
         * > maior
         * < menor
         * >= maior ou igual que
         * <= menor ou igual que
         */

         int valor1 = 1;
         int valor2 = 2;
         System.out.println(valor1 == valor2);
         System.out.println(valor1 != valor2);
         System.out.println(valor1 > valor2);
         System.out.println(valor1 < valor2);
         System.out.println(valor1 >= valor2);
         System.out.println(valor1 <= valor2);
    }
}
